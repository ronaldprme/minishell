#include "../libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	c;
	size_t	d;

	d = 0;
	c = 0;
	while (dst[d] && d < size)
		d++;
	while (src[c] && (c + d +1) < size)
	{
		dst[d + c] = src[c];
		c++;
	}
	if (d < size)
		dst[c + d] = '\0';
	return (d + ft_strlen(src));
}
